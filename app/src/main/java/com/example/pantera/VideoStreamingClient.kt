package com.example.pantera

import android.content.Context
import android.util.Log
import io.socket.client.IO
import io.socket.client.Socket
import org.json.JSONException
import org.json.JSONObject
import org.webrtc.*
import java.net.URISyntaxException

class VideoStreamingClient(
    private val context: Context,
    private val eglBaseContext: EglBase.Context,
    private val peerConnectionFactory: PeerConnectionFactory
) {
    private var socket: Socket? = null
    private var peerConnection: PeerConnection? = null
    private var videoCapturer: CameraVideoCapturer? = null
    private var videoSource: VideoSource? = null
    private var videoTrack: VideoTrack? = null
    private var surfaceTextureHelper: SurfaceTextureHelper? = null  // ‚≠ê CAMBIO: Nullable

    companion object {
        private const val TAG = "VideoStreamingClient"
        private const val VIDEO_TRACK_ID = "ARDAMSv0"
        private const val VIDEO_RESOLUTION_WIDTH = 640
        private const val VIDEO_RESOLUTION_HEIGHT = 480
        private const val VIDEO_FPS = 30
    }

    // Observador para eventos de PeerConnection
    private val peerConnectionObserver = object : PeerConnection.Observer {
        override fun onIceCandidate(iceCandidate: IceCandidate) {
            Log.d(TAG, "onIceCandidate: $iceCandidate")
            val json = JSONObject()
            try {
                json.put("type", "ice_candidate")
                json.put("candidate", iceCandidate.sdp)
                json.put("sdpMid", iceCandidate.sdpMid)
                json.put("sdpMLineIndex", iceCandidate.sdpMLineIndex)
                socket?.emit("ice_candidate", json)
            } catch (e: JSONException) {
                Log.e(TAG, "Error al crear JSON para IceCandidate", e)
            }
        }

        override fun onAddStream(mediaStream: MediaStream) { Log.d(TAG, "onAddStream") }
        override fun onRemoveStream(mediaStream: MediaStream) { Log.d(TAG, "onRemoveStream") }
        override fun onDataChannel(dataChannel: DataChannel) { Log.d(TAG, "onDataChannel") }
        override fun onRenegotiationNeeded() { Log.d(TAG, "onRenegotiationNeeded") }
        override fun onIceConnectionChange(newState: PeerConnection.IceConnectionState) {
            Log.d(TAG, "onIceConnectionChange: $newState")
        }
        override fun onIceConnectionReceivingChange(p0: Boolean) {}
        override fun onIceGatheringChange(newState: PeerConnection.IceGatheringState) {
            Log.d(TAG, "onIceGatheringChange: $newState")
        }
        override fun onIceCandidatesRemoved(candidates: Array<out IceCandidate>) {}
        override fun onSignalingChange(newState: PeerConnection.SignalingState) {
            Log.d(TAG, "onSignalingChange: $newState")
        }
        override fun onAddTrack(receiver: RtpReceiver, mediaStreams: Array<out MediaStream>) {}
    }

    // Observador para eventos de SDP (Offer/Answer)
    private inner class SimpleSdpObserver : SdpObserver {
        override fun onCreateSuccess(sessionDescription: SessionDescription) {
            Log.d(TAG, "SdpObserver onCreateSuccess")
            peerConnection?.setLocalDescription(this, sessionDescription)
            val json = JSONObject()
            try {
                json.put("type", sessionDescription.type.canonicalForm())
                json.put("sdp", sessionDescription.description)
                val eventName = if (sessionDescription.type == SessionDescription.Type.ANSWER) "answer" else "offer"
                socket?.emit(eventName, json)
            } catch (e: JSONException) {
                Log.e(TAG, "Error al crear JSON para SDP", e)
            }
        }
        override fun onSetSuccess() { Log.d(TAG, "SdpObserver onSetSuccess") }
        override fun onCreateFailure(s: String) { Log.e(TAG, "SdpObserver onCreateFailure: $s") }
        override fun onSetFailure(s: String) { Log.e(TAG, "SdpObserver onSetFailure: $s") }
    }


    fun startStreaming(serverUrl: String, deviceId: String) {
        try {
            Log.d(TAG, "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê")
            Log.d(TAG, "üé• Iniciando streaming de video")
            Log.d(TAG, "   Server: $serverUrl")
            Log.d(TAG, "   Device ID: $deviceId")
            Log.d(TAG, "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê")

            val opts = IO.Options()
            opts.query = "deviceId=$deviceId"
            opts.transports = arrayOf("websocket")

            socket = IO.socket(serverUrl, opts)

            setupSocketListeners()
            socket?.connect()

            Log.d(TAG, "‚úÖ Socket WebSocket configurado correctamente")

        } catch (e: URISyntaxException) {
            Log.e(TAG, "‚ùå Error de URI: ${e.message}", e)
        } catch (e: Exception) {
            Log.e(TAG, "‚ùå Error iniciando streaming: ${e.message}", e)
        }
    }

    private fun setupSocketListeners() {
        socket?.on(Socket.EVENT_CONNECT) {
            Log.d(TAG, "‚úÖ Socket conectado exitosamente")
        }?.on(Socket.EVENT_DISCONNECT) {
            Log.d(TAG, "‚ö†Ô∏è Socket desconectado")
            stopStreaming()
        }?.on(Socket.EVENT_CONNECT_ERROR) { args ->
            Log.e(TAG, "‚ùå Error de conexi√≥n: ${args.getOrNull(0)}")
        }?.on("offer") { args ->
            Log.d(TAG, "üì© Oferta SDP recibida del servidor")
            try {
                val sdpJson = args[0] as JSONObject
                val sdpDescription = sdpJson.getString("sdp")
                val sdpType = SessionDescription.Type.fromCanonicalForm(sdpJson.getString("type").lowercase())

                initPeerConnection()

                peerConnection?.setRemoteDescription(SimpleSdpObserver(), SessionDescription(sdpType, sdpDescription))
                createAnswer()
            } catch (e: JSONException) {
                Log.e(TAG, "‚ùå Error al parsear oferta SDP", e)
            }
        }?.on("ice_candidate") { args ->
            Log.d(TAG, "üßä Candidato ICE recibido")
            try {
                val json = args[0] as JSONObject
                val candidate = IceCandidate(
                    json.getString("sdpMid"),
                    json.getInt("sdpMLineIndex"),
                    json.getString("candidate")
                )
                peerConnection?.addIceCandidate(candidate)
                Log.d(TAG, "‚úÖ Candidato ICE a√±adido")
            } catch (e: JSONException) {
                Log.e(TAG, "‚ùå Error al parsear candidato ICE", e)
            }
        }
    }

    private fun initPeerConnection() {
        if (peerConnection != null) {
            Log.d(TAG, "‚ö†Ô∏è PeerConnection ya existe, reutilizando")
            return
        }

        Log.d(TAG, "üîß Inicializando PeerConnection...")

        try {
            // Configuraci√≥n de ICE Servers (STUN)
            val iceServers = listOf(
                PeerConnection.IceServer.builder("stun:stun.l.google.com:19302").createIceServer()
            )

            // Crear PeerConnection
            peerConnection = peerConnectionFactory.createPeerConnection(iceServers, peerConnectionObserver)
            Log.d(TAG, "‚úÖ PeerConnection creada")

            // Inicializar helper de textura y capturador de video
            surfaceTextureHelper = SurfaceTextureHelper.create("VideoCapturerThread", eglBaseContext)
            Log.d(TAG, "‚úÖ SurfaceTextureHelper creado")

            videoCapturer = createCameraCapturer()

            if (videoCapturer == null) {
                Log.e(TAG, "‚ùå No se pudo crear el capturador de video")
                return
            }
            Log.d(TAG, "‚úÖ CameraVideoCapturer creado")

            // Crear fuente de video y track
            videoSource = peerConnectionFactory.createVideoSource(videoCapturer!!.isScreencast)
            videoTrack = peerConnectionFactory.createVideoTrack(VIDEO_TRACK_ID, videoSource)
            Log.d(TAG, "‚úÖ VideoSource y VideoTrack creados")

            // Inicializar y arrancar capturador
            videoCapturer?.initialize(surfaceTextureHelper, context, videoSource!!.capturerObserver)
            videoCapturer?.startCapture(VIDEO_RESOLUTION_WIDTH, VIDEO_RESOLUTION_HEIGHT, VIDEO_FPS)
            Log.d(TAG, "‚úÖ Captura de c√°mara iniciada: ${VIDEO_RESOLUTION_WIDTH}x${VIDEO_RESOLUTION_HEIGHT} @ ${VIDEO_FPS}fps")

            // A√±adir track de video al PeerConnection
            peerConnection?.addTrack(videoTrack)
            Log.d(TAG, "‚úÖ Track de video a√±adido a PeerConnection")

        } catch (e: Exception) {
            Log.e(TAG, "‚ùå Error inicializando PeerConnection: ${e.message}", e)
        }
    }

    private fun createAnswer() {
        Log.d(TAG, "üìù Creando respuesta SDP...")
        val constraints = MediaConstraints()
        constraints.mandatory.add(MediaConstraints.KeyValuePair("OfferToReceiveAudio", "false"))
        constraints.mandatory.add(MediaConstraints.KeyValuePair("OfferToReceiveVideo", "false"))

        peerConnection?.createAnswer(SimpleSdpObserver(), constraints)
    }

    private fun createCameraCapturer(): CameraVideoCapturer? {
        Log.d(TAG, "üé• Buscando c√°maras disponibles...")
        val enumerator = Camera2Enumerator(context)
        val deviceNames = enumerator.deviceNames

        Log.d(TAG, "üì± C√°maras encontradas: ${deviceNames.size}")

        // Intentar encontrar la c√°mara trasera
        for (deviceName in deviceNames) {
            if (enumerator.isBackFacing(deviceName)) {
                Log.d(TAG, "‚úÖ Usando c√°mara trasera: $deviceName")
                return enumerator.createCapturer(deviceName, null)
            }
        }
        // Si no, intentar encontrar la c√°mara frontal
        for (deviceName in deviceNames) {
            if (enumerator.isFrontFacing(deviceName)) {
                Log.d(TAG, "‚úÖ Usando c√°mara frontal: $deviceName")
                return enumerator.createCapturer(deviceName, null)
            }
        }
        // Si no, usar la primera c√°mara disponible
        if (deviceNames.isNotEmpty()) {
            Log.d(TAG, "‚úÖ Usando primera c√°mara disponible: ${deviceNames[0]}")
            return enumerator.createCapturer(deviceNames[0], null)
        }

        Log.e(TAG, "‚ùå No se encontraron c√°maras disponibles")
        return null
    }

    fun stopStreaming() {
        Log.d(TAG, "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê")
        Log.d(TAG, "üõë Deteniendo streaming de video...")
        Log.d(TAG, "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê")

        try {
            // ‚≠ê VERIFICACI√ìN SEGURA ‚≠ê
            if (videoCapturer != null) {
                try {
                    videoCapturer?.stopCapture()
                    Log.d(TAG, "‚úÖ Captura de c√°mara detenida")
                } catch (e: InterruptedException) {
                    Log.e(TAG, "‚ö†Ô∏è Error al detener captura: ${e.message}", e)
                }
                videoCapturer?.dispose()
                videoCapturer = null
                Log.d(TAG, "‚úÖ VideoCapturer liberado")
            }

            // ‚≠ê VERIFICACI√ìN SEGURA ‚≠ê
            surfaceTextureHelper?.let {
                it.dispose()
                surfaceTextureHelper = null
                Log.d(TAG, "‚úÖ SurfaceTextureHelper liberado")
            }

            // ‚≠ê VERIFICACI√ìN SEGURA ‚≠ê
            videoTrack?.let {
                it.dispose()
                videoTrack = null
                Log.d(TAG, "‚úÖ VideoTrack liberado")
            }

            // ‚≠ê VERIFICACI√ìN SEGURA ‚≠ê
            videoSource?.let {
                it.dispose()
                videoSource = null
                Log.d(TAG, "‚úÖ VideoSource liberado")
            }

            // ‚≠ê VERIFICACI√ìN SEGURA ‚≠ê
            peerConnection?.let {
                it.close()
                it.dispose()
                peerConnection = null
                Log.d(TAG, "‚úÖ PeerConnection cerrada y liberada")
            }

            // ‚≠ê VERIFICACI√ìN SEGURA ‚≠ê
            socket?.let {
                it.disconnect()
                it.off()
                socket = null
                Log.d(TAG, "‚úÖ Socket desconectado y liberado")
            }

            Log.d(TAG, "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê")
            Log.d(TAG, "‚úÖ Streaming completamente detenido")
            Log.d(TAG, "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê")

        } catch (e: Exception) {
            Log.e(TAG, "‚ùå Error deteniendo streaming: ${e.message}", e)
        }
    }
}